export const getAllProvinces = () => {
  return Object.keys(provincesAndCities);
};

export const getCitiesByProvince = (province: string) => {
  return provincesAndCities[province as keyof typeof provincesAndCities] || [];
};

export const searchCity = (query: string) => {
  const results: { province: string; city: string }[] = [];
  const lowerQuery = query.toLowerCase();
  
  Object.entries(provincesAndCities).forEach(([province, cities]) => {
    cities.forEach(city => {
      if (city.toLowerCase().includes(lowerQuery)) {
        results.push({ province, city });
      }
    });
  });
  
  return results;
};

export const isProvinceValid = (province: string) => {
  return province in provincesAndCities;
};

export const isCityValid = (province: string, city: string) => {
  const cities = provincesAndCities[province as keyof typeof provincesAndCities];
  return cities ? cities.includes(city) : false;
};